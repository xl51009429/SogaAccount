	}
	
	class var H12: UIFont{
		return self.systemFont(ofSize: 12)
	}
	
	class var H13: UIFont{
		return self.systemFont(ofSize: 13)
	}
	
	class var H14: UIFont{
		return self.systemFont(ofSize: 14)
	}
	
	class var H15: UIFont{
		return self.systemFont(ofSize: 15)
	}
	
	class var H16: UIFont{
		return self.systemFont(ofSize: 16)
	}
}
